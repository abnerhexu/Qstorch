@pytest.mark.task4_4
@given(tensors())
def test_drop(t: Tensor) -> None:
    q = qstorch.dropout(t, 0.0)
    idx = q._tensor.sample()
    assert q[idx] == t[idx]
    q = qstorch.dropout(t, 1.0)
    assert q[q._tensor.sample()] == 0.0
    q = qstorch.dropout(t, 1.0, ignore=True)
    idx = q._tensor.sample()
    assert q[idx] == t[idx]


@pytest.mark.task4_4
@given(tensors(shape=(1, 1, 4, 4)))
def test_softmax(t: Tensor) -> None:
    q = qstorch.softmax(t, 3)
    x = q.sum(dim=3)
    assert_close(x[0, 0, 0, 0], 1.0)

    q = qstorch.softmax(t, 1)
    x = q.sum(dim=1)
    assert_close(x[0, 0, 0, 0], 1.0)

    qstorch.grad_check(lambda a: qstorch.softmax(a, dim=2), t)


@pytest.mark.task4_4
@given(tensors(shape=(1, 1, 4, 4)))
def test_log_softmax(t: Tensor) -> None:
    q = qstorch.softmax(t, 3)
    q2 = qstorch.logsoftmax(t, 3).exp()
    for i in q._tensor.indices():
        assert_close(q[i], q2[i])

    qstorch.grad_check(lambda a: qstorch.logsoftmax(a, dim=2), t)
